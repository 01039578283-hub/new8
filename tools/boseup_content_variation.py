def _pick(generator: Any, key: str, slot: str, values: tuple[str, ...]) -> str:
    return generator.stable_pick(key, slot, list(values))


def _vary_phrases(generator: Any, value: str, key: str) -> str:
    result = value
    for index, (needle, options) in enumerate(PHRASE_BANKS):
        if needle not in result:
            continue
        result = result.replace(
            needle,
            _pick(generator, key, f"boseup-phrase-{index}", options),
        )
    return result


def _heading_kind(value: str) -> str:
    if any(token in value for token in ("수업학교", "학교별", "학교 일정", "학교 진도")):
        return "school"
    if any(token in value for token in ("등원", "위치", "주소", "생활 리듬")):
        return "location"
    if any(token in value for token in ("상담", "준비", "선택 전")):
        return "consultation"
    if any(token in value for token in ("학생 유형", "학습 원인", "진단")):
        return "diagnosis"
    if any(token in value for token in ("복습", "과제", "오답", "독립 풀이", "관리 구조")):
        return "management"
    if any(token in value for token in ("변화", "학습 기록", "초기 학습")):
        return "change"
    return ""


def _vary_heading(
    generator: Any,
    heading: str,
    title: str,
    local: str,
    key: str,
) -> str:
    kind = _heading_kind(heading)
    if not kind:
        return heading
    return _pick(
        generator,
        key,
        f"boseup-heading-{kind}",
        HEADING_BANKS[kind],
    ).format(TITLE=title, LOCAL=local)


def _faq_kind(question: str, answer: str) -> str:
    combined = f"{question} {answer}"
    if any(token in combined for token in ("학교", "교과서", "범위표", "내신")):
        return "school"
    if any(token in combined for token in ("주소", "등원", "동선", "귀가", "이동")):
        return "location"
    if any(token in combined for token in ("성적", "향상", "보장", "성과", "상승")):
        return "outcome"
    if any(
        token in combined
        for token in ("어떤 학생", "학생 유형", "학습 습관", "적합", "어려움")
    ):
        return "fit"
    return "topic"


def _topic_from_question(question: str) -> str:
    match = re.search(
        r"(.+?)(?:은|는)\s*(?:어떤 내용을|무엇을)\s*확인해야",
        question,
    )
    if match:
        topic = match.group(1).strip()
        if 1 < len(topic) <= 24:
            return topic
    for token in (
        "학원혜택",
        "입시자료분석",
        "일일학습점검",
        "학습문제관리",
        "학원보강",
        "내신시험관리",
        "진도관리반",
        "보충수업",
        "학습관리",
    ):
        if token in question:
            return token
    return "해당 안내 항목"


def _vary_question(
    generator: Any,
    question: str,
    answer: str,
    title: str,
    local: str,
    key: str,
) -> tuple[str, str]:
    kind = _faq_kind(question, answer)
    topic = _topic_from_question(question)
    start = _pick(
        generator,
        key,
        f"boseup-faq-start-{kind}",
        QUESTION_STARTS[kind],
    )
    core = _pick(
        generator,
        key,
        f"boseup-faq-core-{kind}",
        QUESTION_CORES[kind],
    )
    varied_question = (start + core).format(
        TITLE=title,
        LOCAL=local,
        TOPIC=topic,
    )

    if kind in ANSWER_PARTS:
        first, second = ANSWER_PARTS[kind]
        lead = (
            _pick(
                generator,
                key,
                f"boseup-faq-answer-lead-{kind}",
                first,
            )
            + " "
            + _pick(
                generator,
                key,
                f"boseup-faq-answer-focus-{kind}",
                second,
            )
        ).format(TITLE=title, LOCAL=local)
        varied_answer = f"{lead} {answer}"
    else:
        varied_answer = answer
    varied_question = varied_question.replace("관리을", "관리를")
    varied_answer = _vary_phrases(
        generator,
        varied_answer,
        key + "|answer",
    )
    return varied_question, varied_answer.replace("관리을", "관리를")


def prepare_boseup_source_copy(
    generator: Any,
    sections: dict[str, str],
    row: dict[str, str],
) -> tuple[
    dict[str, str],
    str,
    list[tuple[str, list[str]]],
    list[tuple[str, str]],
    str,
    list[str],
]:
    """Return the source copy with deterministic, category-only variation."""
    (
        prepared,
        intro,
        blocks,
        faqs,
        review_note,
        reviews,
    ) = generator.prepare_source_copy(sections, row)

    title = sections["페이지타이틀"].strip()
    local = generator.locality_from_title(title)
    intro = _vary_phrases(generator, intro, title + "|intro")

    varied_blocks: list[tuple[str, list[str]]] = []
    for block_index, (heading, paragraphs) in enumerate(blocks):
        block_key = f"{title}|boseup-block-{block_index}"
        varied_heading = _vary_heading(
            generator,
            heading,
            title,
            local,
            block_key,
        )
        varied_paragraphs = [
            _vary_phrases(
                generator,
                paragraph,
                f"{block_key}|paragraph-{paragraph_index}",
            )
            for paragraph_index, paragraph in enumerate(paragraphs)
        ]
        varied_blocks.append((varied_heading, varied_paragraphs))

    varied_faqs = [
        _vary_question(
            generator,
            question,
            answer,
            title,
            local,
            f"{title}|boseup-faq-{index}",
        )
        for index, (question, answer) in enumerate(faqs)
    ]
    varied_reviews = [
        _vary_phrases(
            generator,
            review,
            f"{title}|boseup-review-{index}",
        )
        for index, review in enumerate(reviews)
    ]
    return (
        prepared,
        intro,
        varied_blocks,
        varied_faqs,
        review_note,
        varied_reviews,
    )
