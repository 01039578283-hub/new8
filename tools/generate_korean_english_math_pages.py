def related_for(
    row: dict[str, str], rows: list[dict[str, str]]
) -> list[tuple[str, str, str]]:
    local = row["근처 수업가능 동네"].strip()
    district = row.get("시or구", "").strip()
    region = row.get("지역", "").strip()
    source = [
        item
        for item in rows
        if item.get("지역", "").strip() == region
        and item.get("시or구", "").strip() == district
        and item["근처 수업가능 동네"].strip() != local
    ]
    if len(source) < 6:
        source.extend(
            item
            for item in rows
            if item.get("지역", "").strip() == region
            and item["근처 수업가능 동네"].strip() != local
        )
    result: list[tuple[str, str, str]] = []
    seen: set[str] = set()
    for item in source:
        name = item["근처 수업가능 동네"].strip()
        if not name or name in seen:
            continue
        seen.add(name)
        result.append(
            (
                f"{name} {CATEGORY}",
                absolute_url(PARENT, CATEGORY, slug_ko(name)),
                compact_text(item.get("시or구"))
                or compact_text(item.get("지역"))
                or "다른 지역",
            )
        )
        if len(result) == 6:
            break
    return result


def sibling_category_links(local: str) -> list[tuple[str, str, str]]:
    slug = slug_ko(local)
    links: list[tuple[str, str, str]] = []
    for name, profile in available_categories():
        if name == CATEGORY:
            continue
        page = SITE / PARENT / name / slug / "index.html"
        if page.exists():
            links.append(
                (
                    f"{local} {name}",
                    absolute_url(PARENT, name, slug),
                    str(profile["card_label"]),
                )
            )
    return links


